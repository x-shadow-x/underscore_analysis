  // Internal function that returns an efficient (for current engines) version
  // of the passed-in callback, to be repeatedly applied in other Underscore
  // functions.
  // 此为内部调用的函数~简单来说下面这个函数是对bind写法的一个处理~或是优化~额~也方便调用吧
  // 参数列表的意思是由指定的context来执行func函数，而func函数的参数为argCount
  // 前面额外添加switch-case处理的四种情况是为了在已知参数数量的情况下让 js 引擎做出优化（避免使用 arguments）
  // 若不考虑性能的提升~完全可以用最后那个return function() { return func.apply(context, arguments); };来处理
  // 具体测试代码可查看 optimizeCbTest.js
  var optimizeCb = function(func, context, argCount) {
    if (context === void 0) return func;
    switch (argCount) {
      case 1:
        return function(value) {
          return func.call(context, value);
        };
        // The 2-parameter case has been omitted only because no current consumers
        // made use of it.
      case null:
      case 3:
        return function(value, index, collection) {
          return func.call(context, value, index, collection);
        };
      case 4:
        return function(accumulator, value, index, collection) {
          return func.call(context, accumulator, value, index, collection);
        };
    }
    return function() {
      return func.apply(context, arguments);
    };
  };

  var builtinIteratee;

  // An internal function to generate callbacks that can be applied to each
  // element in a collection, returning the desired result — either `identity`,
  // an arbitrary callback, a property matcher, or a property accessor.
  // 此函数的作用是根据传进的 value情况的不同返回不一样的结果：
  // 如果传进来的value是一个函数，则调用optimizeCb返回一个高阶函数
  // 如果传进来的value是一个对象，则使用_.matcher生成一个断言函数~断言函数的作用是判断所给对象是否具有value的所有的key-value对
  // 举例来说假设有这样的调用var predicate = _.matcher({selected: true, visible: true});此时predicate就是一个断言函数
  // 函数体为function(obj) {return _.isMarch(obj, attrs)} 其中的attrs就是上面的{selected: true, visible: true}
  // 假设我们有另一个变量 var another = {name:'Xx',age: 18,.....}当我们调用断言函数时，即 predicate(another)
  // 函数做的事情是判断 another这个对象是否包含{selected: true, visible: true}这两个键值对，若包含，则返回true否则返回false
  // 而_.property的函数声明在1500行左右，从中可以看到_.property(value)的结果也是一个函数
  // function(obj) {return obj == null ? void 0 : obj[key];}
  // 假设有如下调用：
  // var result = _.property('name');
  // var obj = {name: 'Xx', age: 18};
  // result(obj);
  // 则会得到Xx的结果，也即是说_.property函数返回一个可以得到对象指定属性的函数
  var cb = function(value, context, argCount) {
    if (_.iteratee !== builtinIteratee) return _.iteratee(value, context);
    if (value == null) return _.identity;
    if (_.isFunction(value)) return optimizeCb(value, context, argCount);
    if (_.isObject(value) && !_.isArray(value)) return _.matcher(value);
    return _.property(value);
  };

  // External wrapper for our callback generator. Users may customize
  // `_.iteratee` if they want additional predicate/iteratee shorthand styles.
  // This abstraction hides the internal-only argCount argument.
  _.iteratee = builtinIteratee = function(value, context) {
    return cb(value, context, Infinity);
  };

  // Similar to ES6's rest param (http://ariya.ofilabs.com/2013/03/es6-and-rest-parameter.html)
  // This accumulates the arguments passed into an array, after a given index.
  // Similar to ES6's rest param (http://ariya.ofilabs.com/2013/03/es6-and-rest-parameter.html)
  // This accumulates the arguments passed into an array, after a given index.
  // 用来模仿es6的剩余参数，se6中假设有如下声明 var func = function(a, b, ...myRestArgs) {}; func(1,2,3,4,5);
  // 此时func的形参列表中 a = 1, b = 2, myRestArgs = [3, 4, 5]; 和 arguments不同，myRestArgs是一个真正的数组
  // 这里restArgs就是用来模拟es6的这个特性的，有多个函数使用到 restArgs 以_.invoke为例
  // _.invoke = restArgs(function(obj, method, args) {。。。。}），此时startIndex为 null，于是在 restArgs中的第一行得到修正（函数的 length属性代表它的形参的个数）
  // startIndex代表从第几个参数开始将后面的参数作为剩余参数，
  var restArgs = function(func, startIndex) {
    startIndex = startIndex == null ? func.length - 1 : +startIndex;
    //以_.invoke为例，下面返回一个函数并将其赋值给_.invoke
    //假设忽略_.invoke的正确用法，当我们调用_.invoke函数的时候，假设我们传递了很多参数过去，比如_.invoke(1,2,3,4,5,6);
    //此时的效果就是 startIndex在第一行根据 func即function(obj, method, args) {。。。。。}的形参个数被修正为 2
    //调用函数_.invoke(1,2,3,4,5,6);则下面的arguments.length为6，于是得到length为4，接着我们初始化一个有4个元素长度的数组rest
    //然后把arguments中的第 2项以后的所有的参数都存到 rest，当我们存好 rest后就去调用 func
    //此时你会发现，_.invoke(1,2,3,4,5,6);的前两个参数分别赋值给了obj和 method，剩下的参数全部赋值给了args
    //就是这么奇妙，我们调用 restArgs的时候会传入一个函数(func)作为第一参数，如果不指定 startIndex那 restArgs就默认将 startIndex设置为func参数个数减 1
    //这个操作的效果此时并没有怎么体现出来，当我们去调用 restArgs返回的函数的时候很妙的事就发生了，默认情况下，多余的参数就会被存成数组并被传递给func的最后一个参数
    //因为 restArgs做的事除了修正 startIndex外就是用一个匿名函数包住func并返回，以达到func的形参有剩余参数的效果
    //而最后真正完成所需功能的操作是由func来做的
    // 有个博客也有说明 http://www.jianshu.com/p/938ba29667c3
    return function() {
      var length = Math.max(arguments.length - startIndex, 0),
        rest = Array(length),
        index = 0;
      for (; index < length; index++) {
        rest[index] = arguments[index + startIndex];
      }
      switch (startIndex) {
        case 0:
          return func.call(this, rest);
        case 1:
          return func.call(this, arguments[0], rest);
        case 2:
          return func.call(this, arguments[0], arguments[1], rest);
      }
      // 当 startIndex很大的时候，那我们就不干了，直接用apply将所有的参数一次性传过去~此时就没有剩余参数的效果了
      // 不然如果startIndex真的很大的话，意味着我们要使用func.call来调用func以便把 rest之前的参数一个一个写出来传过去
      var args = Array(startIndex + 1);
      for (index = 0; index < startIndex; index++) {
        args[index] = arguments[index];
      }
      args[startIndex] = rest;
      return func.apply(this, args);
    };
  };

  // Invoke a method (with arguments) on every item in a collection.
  // 此函数原本声明在300多行，现将其写到这里方便于上面的 restArgs函数一起分析
  // _.invoke调用的时候第一个参数是一个集合，可以是数组，也可以是对象
  // 第二个参数是一个方法名，此方法可以是一个全局方法，也可以是第一个参数的每一项的成员方法
  // 如果是全局方法，则第二个参数传的是方法名，如果是第一个参数的每一项的成员方法，则传的是方法名的字符串
  // 整个函数的效果就是遍历第一个参数的每一项，将其作为 method方法的执行上下文并执行 method方法
  // _.invoke还有第三个参数，而 _.invoke被调用的时候可以传递多个参数，多余的参数因为 restArgs的作用全部被放到一个数组里并被传递给 args
  _.invoke = restArgs(function(obj, path, args) {
    var contextPath, func;
    if (_.isFunction(path)) {
      func = path;
    } else if (_.isArray(path)) {
      contextPath = path.slice(0, -1);
      path = path[path.length - 1];
    }
    return _.map(obj, function(context) {
      var method = func;
      if (!method) {
        if (contextPath && contextPath.length) {
          context = deepGet(context, contextPath);
        }
        if (context == null) return void 0;
        method = context[path];
      }
      return method == null ? method : method.apply(context, args);
    });
  });

  // An internal function for creating a new object that inherits from another.
  // Ctor是一个裸函数，在前面声明为 var Ctor = function() {}; 顺带提下 prototype属性：
  // 每个函数都有一个prototype属性，这个属性是指向一个对象的引用，这个对象称为原型对象，原型对象包含函数实例共享的方法和属性，
  // 也就是说将函数用作构造函数调用（使用new操作符调用）的时候，新创建的对象会从原型对象上继承属性和方法。
  var baseCreate = function(prototype) {
    if (!_.isObject(prototype)) return {};
    if (nativeCreate) return nativeCreate(prototype);
    Ctor.prototype = prototype;
    var result = new Ctor;
    Ctor.prototype = null;
    return result;
  };

  // 用 void 0代替 undefined 避免在低版本浏览器中出现的 undefined重写问题，也节约了字符~毕竟 viod 0比 undefined短嘛
  // 不少 JavaScript 压缩工具在压缩过程中，也正是用 void 0替代 undefined的
  var shallowProperty = function(key) {
    return function(obj) {
      return obj == null ? void 0 : obj[key];
    };
  };

  var deepGet = function(obj, path) {
    var length = path.length;
    for (var i = 0; i < length; i++) {
      if (obj == null) return void 0;
      obj = obj[path[i]];
    }
    return length ? obj : void 0;
  };

  // Helper for collection methods to determine whether a collection
  // should be iterated as an array or as an object.
  // Related: http://people.mozilla.org/~jorendorff/es6-draft.html#sec-tolength
  // Avoids a very nasty iOS 8 JIT bug on ARM-64. #2094
  // 如果集合有Length属性且为数字并且大于0小于最大的精确整数，则判定是类数组
  // 原来 isArrayLike的判断条件并没有(length - 1) in collection，这就导致在obj = {name:'Xx', length: 1}也被判断为类数组~
  // 然后_.each便会根据数字下表来遍历集合~但是此时集合是不能通过下标来访问的~将导致_.each无法遍历
  // 故在源码的基础上加上(length - 1) in collection~以便让obj = {name:'Xx', length: 1}在_.each中可以通过对象的方式遍历
  var MAX_ARRAY_INDEX = Math.pow(2, 53) - 1;
  var getLength = shallowProperty('length');
  var isArrayLike = function(collection) {
    var length = getLength(collection);
    return typeof length == 'number' && length >= 0 && length <= MAX_ARRAY_INDEX && (length - 1) in collection;
  };

